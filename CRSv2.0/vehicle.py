class Vehicle:
    def __init__(
        self,
        __vehicleID,
        __make,
        __model,
        __year,
        __dailyRate,
        __status,
        __passengerCapacity,
        __engineCapacity,
    ):
        self.vehicleID = __vehicleID
        self.make = __make
        self.model = __model
        self.year = __year
        self.dailyRate = __dailyRate
        self.status = __status
        self.passengerCapacity = __passengerCapacity
        self.engineCapacity = __engineCapacity
